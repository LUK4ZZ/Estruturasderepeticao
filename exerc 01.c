#include<stdio.h>

int main(){


    //. Faça um programa que leia um número inteiro positivo N e imprima todos os números naturais de 0 até N em ordem crescente. 
    
    int numero;

    printf("Digite um numero:");
    scanf("%d", &numero);

    for(int c = 0; c <= numero; c++){

        printf("\n%d", c);
    }





}